use crate::utils::seeder;
use wasm_bindgen::prelude::*;

#[wasm_bindgen(js_name = internet_emoji)]
pub fn emoji() -> String {
	let format = seeder::gen_range(0..10);

	match format {
		0 => smiley_emoji(),
        1 => body_emoji(),
        2 => person_emoji(),
        3 => nature_emoji(),
        4 => food_emoji(),
        5 => travel_emoji(),
        6 => activity_emoji(),
        7 => object_emoji(),
        8 => symbol_emoji(),
        9 => flag_emoji(),
        _ => "".to_string(),
	}
}

